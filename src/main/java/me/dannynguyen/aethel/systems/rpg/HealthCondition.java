/**
 * Types of health conditions.
 *
 * @author Danny Nguyen
 * @version 1.15.4
 * @since 1.13.10
 */
enum HealthCondition {
  /**
   * Below max health.
   */
  WOUNDED,

  /**
   * At max health.
   */
  NORMAL,

  /**
   * Above max health.
   */
  OVERSHIELD
}
