package me.dannynguyen.aethel.systems.rpg;

/**
 * Types of Aethel attributes.
 *
 * @author Danny Nguyen
 * @version 1.13.1
 * @since 1.9.10
 */
public enum AethelAttribute {
  /**
   * Max health.
   */
  MAX_HP,

  /**
   * Critical hit chance.
   */
  CRITICAL_CHANCE,

  /**
   * Critical hit damage multiplier.
   */
  CRITICAL_DAMAGE,

  /**
   * Counterattack chance.
   */
  COUNTER_CHANCE,

  /**
   * Dodge chance.
   */
  DODGE_CHANCE,

  /**
   * Flat damage blocked.
   */
  TOUGHNESS,

  /**
   * Item damage multiplier.
   */
  ITEM_DAMAGE,

  /**
   * Item cooldown multiplier.
   */
  ITEM_COOLDOWN
}
