package me.dannynguyen.aethel.systems.plugin;

/**
 * Plugin player metadata.
 *
 * @author Danny Nguyen
 * @version 1.17.3
 * @since 1.10.1
 */
public enum PlayerMeta {
  /**
   * Action input type.
   */
  ACTION,

  /**
   * Menu category.
   */
  CATEGORY,

  /**
   * Developer mode.
   */
  DEVELOPER,

  /**
   * Future action.
   */
  FUTURE,

  /**
   * Menu type.
   */
  INVENTORY,

  /**
   * Message input type.
   */
  MESSAGE,

  /**
   * Menu page.
   */
  PAGE,

  /**
   * Target player.
   */
  PLAYER,

  /**
   * Equipment slot.
   */
  SLOT,

  /**
   * Type of condition.
   */
  CONDITION,

  /**
   * Type of interacting object.
   */
  TYPE
}