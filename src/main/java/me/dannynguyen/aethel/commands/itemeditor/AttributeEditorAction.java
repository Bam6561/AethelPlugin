/**
 * Types of interactions within AttributeEditor menus.
 *
 * @author Danny Nguyen
 * @version 1.13.1
 * @since 1.9.18
 */
public enum AttributeEditorAction {
  /**
   * Head slot.
   */
  HEAD,

  /**
   * Chest slot.
   */
  CHEST,

  /**
   * Legs slot.
   */
  LEGS,

  /**
   * Feet slot.
   */
  FEET,

  /**
   * Hand slot.
   */
  HAND,

  /**
   * Off hand slot.
   */
  OFF_HAND,

  /**
   * Necklace slot.
   */
  NECKLACE,

  /**
   * Ring slot.
   */
  RING
}
